package com.example.demor2dbc.exceptions;

public class IllegalAccessOperation extends IllegalArgumentException {


	public IllegalAccessOperation() {
		super();
	}

	public IllegalAccessOperation(String message, Throwable cause) {
		super(message, cause);
	}

	public IllegalAccessOperation(String s) {
		super(s);
	}

	public IllegalAccessOperation(Throwable cause) {
		super(cause);
	}

}